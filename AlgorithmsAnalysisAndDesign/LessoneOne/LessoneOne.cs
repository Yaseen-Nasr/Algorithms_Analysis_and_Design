using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace AlgorithmsAnalysisAndDesign.LessoneOne
{
    public static class LessoneOne
    {
        public static double ParallelogramArea(double parallelogramBase, double parallelogramhight)
        {
            return parallelogramBase * parallelogramhight;
        }
        public static double trapezoidArea(double base1, double base2, double height)
        {
            return .5 * (base1 + base2) * height;
        }

    }
}
